#include <stdio.h>

int soma_diagonal1(int a[20][20], int n) {
    int total = 0;

    for (int i = 0; i < n; i++) {
        total += a[i][i];
    }

    return total;
}


int soma_diagonal2(int a[20][20], int n) {
    int total = 0;

    for (int i = 0; i < n; i++) {
        total += a[i][n - i - 1];
    }

    return total;
}

int soma_coluna(int a[20][20], int n, int col) {
    int total = 0;

    for (int i = 0; i < n; i++) {
        total += a[i][col];
    }

    return total;
}

int soma_linha(int a[20][20], int n, int lin) {
    int total = 0;

    for (int i = 0; i < n; i++) {
        total += a[lin][i];
    }

    return total;
}

int magico(int a[20][20], int n) {
    int soma = soma_diagonal1(a, n);

    if (soma != soma_diagonal2(a, n)) {
        return 0;
    }

    for (int i = 0; i < n; i++) {
        if (soma != soma_coluna(a, n, i)) {
            return 0;
        } if (soma != soma_linha(a, n, i)) {
            return 0;
        }
    }

    return 1;
}

